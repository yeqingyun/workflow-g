package com.gionee.gniflow.biz.model;

public class JTeamMonth {
	
	private Integer id;
	
	private String name;
	
	private Integer apply_month;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getApply_month() {
		return apply_month;
	}

	public void setApply_month(Integer apply_month) {
		this.apply_month = apply_month;
	}
	
	
}
